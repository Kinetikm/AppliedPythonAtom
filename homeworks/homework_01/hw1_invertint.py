def reverse(number):
    '''
    Метод, принимающий на вход int и
    возвращающий инвертированный int
    :param number: исходное число
    :return: инвертированное число
    '''
    while number % 10 == 0:
        number = number / 10
    return (str(int(number)))[::-1]
